package com.urremote.invoker.shared;

public class InvalidDeviceStateException extends Exception {
	
	private static final long serialVersionUID = -5669256476693742829L;

	public InvalidDeviceStateException() {
		super();
	}

	public InvalidDeviceStateException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public InvalidDeviceStateException(String arg0) {
		super(arg0);
	}

	public InvalidDeviceStateException(Throwable arg0) {
		super(arg0);
	}
	
	

}
